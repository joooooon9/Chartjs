//다른 방법으로 구해보기 (object.gropuby로 구현한것)
function cal_temps(arr) {
    const today_temp = arr.map(entry => ({
        time: entry.time.split(":")[0].trim(),
        temp: parseFloat(entry.temp)
    }));

    const grouping = Object.groupBy(today_temp, ({ time }) => time);

    let avg = Object.keys(grouping).map(time => {
        let temps = grouping[time].map(entry => entry.temp);
        let avg = temps.reduce((sum, temp) => sum + temp, 0) / temps.length;
        return { time, temp: avg.toFixed(2) };
    })
    return avg;
}

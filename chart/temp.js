// 시간 별 평균 온도 구하기 (reduce로 groupBy를 구현한 것)
function test(arr) {
    // 1-1. 분 단위 시간만 추출
    let hours_temp = arr.map(entry => ({
        hour: entry.time.split(":")[0].trim(), // 시간만 추출
        temp: parseFloat(entry.temp) // 원래 온도값 유지
    }));

    // 1-2. 시간이 같은 object만 묶기 (reduce로 groupBy 구현)
    let group_temp = hours_temp.reduce((acc, curr) => {
        let { hour } = curr;
        if (!acc[hour]) acc[hour] = []; // 키가 없으면 초기화
        acc[hour].push(curr);
        return acc;
    }, {});

    // 2. 그룹화된 temp 평균 구하기
    let avg_temp = Object.keys(group_temp).map(hour => {
        let temps = group_temp[hour].map(entry => entry.temp); // 해당 시간대 온도 배열
        let avg = temps.reduce((sum, temp) => sum + temp, 0) / temps.length; // 평균 계산
        return { hour, temp: avg.toFixed(2) }; // 소수점 2자리까지 유지
    });
    return avg_temp;
}
